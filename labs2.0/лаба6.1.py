print("Рядки. Завдання 1. Після кожного від’ємного елемента вставити новий елемент, рівний квадрату цього від’ємного елементу.")
a = [1, -11, 23, 0, 8, -10, -5, 56]

def min(a, i, b):
    if i == len(a):
        return b
    else: 
        if a[i] >= 0:
            b.append(a[i])
        else:
            b.append(a[i])
            b.append(a[i]*a[i])
        return min(a, i+1, b)

